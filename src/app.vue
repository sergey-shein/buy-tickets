      if (event.target.className.baseVal === "selected") {
        this.places.forEach(element => {
          if (element.id == event.target.id) {
            element.status = "free";
          }
        });

        await axios.patch(`http://localhost:3000/places/${event.target.id}`, { status: "free" });
      } else {
        this.places.forEach(element => {
          if (element.id == event.target.id) {
            element.status = "selected";
          }
        });

        await axios.patch(`http://localhost:3000/places/${event.target.id}`, { status: "selected" });
      }
